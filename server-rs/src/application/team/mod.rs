/**
 * team 应用模块。
 *
 * 作用：
 * 1. 做什么：集中承载组队只读链路的应用服务实现，包括当前队伍、队伍详情、申请列表、附近队伍、大厅列表与收到的邀请。
 * 2. 做什么：把首页聚合与独立 `team` 路由共用的查询逻辑收口成一个模块，避免重复拼接成员、月卡和在线状态。
 * 3. 不做什么：不在这一轮承接创建/退队/申请/邀请等写路径，也不负责 socket 推送。
 *
 * 输入 / 输出：
 * - 输入：角色 ID、队伍 ID，以及附近/大厅接口的可选查询条件。
 * - 输出：供 HTTP 路由与首页聚合复用的组队 DTO。
 *
 * 数据流 / 状态流：
 * - HTTP / 首页聚合 -> 本模块查 PostgreSQL + session_registry -> 组装 Node 兼容队伍视图 -> 返回调用方。
 *
 * 复用设计说明：
 * - 原本首页 `game` 内部已经有一套队伍读取逻辑；抽到这里后，首页与 `/api/team` 复用同一份真值，后续补写路径时也能继续复用成员/申请/邀请查询。
 * - 月卡激活态与在线状态都在本模块内部集中补齐，避免其它模块再记忆同样的 join 规则。
 *
 * 关键边界条件与坑点：
 * 1. 该模块只复刻 Node 当前可见读协议，不偷偷扩展更多字段，避免前端 shape 漂移。
 * 2. `team_members` 真相优先于 `teams`；角色若找不到 team_member，就必须视为未加入队伍，而不是根据其它表猜测关联。
 */
