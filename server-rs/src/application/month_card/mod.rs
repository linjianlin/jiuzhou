/**
 * month_card 应用模块。
 *
 * 作用：
 * 1. 做什么：集中承载月卡状态、激活、每日领取三个接口对应的应用服务实现。
 * 2. 做什么：复用 Node 权威静态配置与数据库表，避免路由层重复维护月卡定义、权益映射和库存扣减规则。
 * 3. 不做什么：不承接尚未迁移的成就推进、角色推送和体力缓存失效副作用。
 *
 * 输入 / 输出：
 * - 输入：由路由层传入的 `user_id`、`month_card_id`、`item_instance_id`。
 * - 输出：供 HTTP 路由直接透传的月卡状态 / 激活结果 / 领取结果 DTO。
 *
 * 数据流 / 状态流：
 * - HTTP 鉴权成功 -> `service` 读取静态定义与 PostgreSQL -> 输出 Node 兼容包体。
 *
 * 复用设计说明：
 * - 月卡静态定义、权益换算、日期键生成与道具扣减都集中在本模块，后续首页聚合或挂机权益读取可以直接复用同一份真值。
 * - 业务规则只保留一个入口，避免 `monthcard/status`、`claim`、`idle` 各自再复制一套定义读取逻辑。
 *
 * 关键边界条件与坑点：
 * 1. 激活与领取都需要事务内二次校验，不能只靠预读结果，否则并发请求会造成双领或双扣。
 * 2. 月卡定义只认启用中的 Node 权威 seed，不能在缺配置时默默返回空权益。
 */
pub mod service;
