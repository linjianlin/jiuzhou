/**
 * tower 应用模块。
 *
 * 作用：
 * 1. 做什么：承载 `/api/tower` 相关的应用服务实现，对齐 Node 端塔概览与排行的只读业务逻辑。
 * 2. 做什么：把塔进度读取、楼层预览生成、排行查询集中在一处，避免 HTTP 层重复拼装 SQL 与怪物池算法。
 * 3. 不做什么：不在这里实现开战、推进结算或 Redis 回写；这些链路仍属于后续迁移范围。
 *
 * 输入 / 输出：
 * - 输入：用户 ID、可选排行 limit，以及静态怪物配置 / 塔冻结快照。
 * - 输出：Node 兼容的塔概览与排行 DTO。
 *
 * 数据流 / 状态流：
 * - HTTP 路由鉴权 -> 本模块读取 PostgreSQL + 运行时恢复索引 -> 输出统一 DTO。
 *
 * 复用设计说明：
 * - 楼层预览算法和冻结池解析都只在本模块维护一份，后续补 `/api/tower/challenge/start` 时可以继续直接复用。
 * - 运行中会话展示复用 battle-session 的公共 snapshot view，避免塔路由再维护一份平行字段协议。
 *
 * 关键边界条件与坑点：
 * 1. 概览中的 `activeSession` 只在 battle runtime 仍存在时对外暴露，不能把失效 battleId 原样返回给前端。
 * 2. 冻结前沿与怪物池必须保持同一条前沿值口径，不能混用不同 frozen floor 的快照。
 */
