/**
 * battle_pass 应用模块。
 *
 * 作用：
 * 1. 做什么：集中承载战令任务概览、任务完成、赛季状态与奖励列表四个 HTTP 端点对应的业务实现。
 * 2. 做什么：复用 Node 权威战令种子与 PostgreSQL 进度表，避免路由层重复维护赛季解析、周期重置和奖励展示映射。
 * 3. 不做什么：不在本模块实现战令奖励领取入包链路，也不承接首页聚合、推送广播等跨域副作用。
 *
 * 输入 / 输出：
 * - 输入：路由层传入的 `user_id`、可选 `season_id` 与 `task_id`。
 * - 输出：供 HTTP 路由直接透传的任务概览、赛季状态、奖励列表与任务完成结果 DTO。
 *
 * 数据流 / 状态流：
 * - HTTP 鉴权成功 -> `service` 读取战令种子与 PostgreSQL -> 组装 Node 兼容协议 -> 路由输出统一 envelope。
 *
 * 复用设计说明：
 * - 赛季解析、周期判断、奖励展示元数据和任务进度聚合都集中在这里，后续若补首页聚合或活动页，不需要再复制一套战令规则。
 * - 静态种子与物品展示索引走模块级缓存，避免每次请求重复读大 JSON。
 *
 * 关键边界条件与坑点：
 * 1. Daily / weekly 任务是否已完成、已领取必须继续按当前周期判断，不能直接信任历史行上的布尔字段。
 * 2. 当前赛季未激活时仍要按 Node 现状回退到配置里的默认赛季，而不是直接返回空赛季。
 */
