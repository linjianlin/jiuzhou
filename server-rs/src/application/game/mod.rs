/**
 * game 首页聚合应用模块。
 *
 * 作用：
 * 1. 做什么：承载 `/api/game/home-overview` 需要的首页聚合读取能力，把首页首屏所需的签到、成就、任务、主线、队伍等高频摘要收敛到单一入口。
 * 2. 做什么：优先复用现有 sign_in/account/realm/inventory/idle 服务，避免首页聚合再次散落复制这些模块的读取逻辑。
 * 3. 不做什么：不替代任务、主线、队伍等完整领域接口，也不在这里实现详情页或写操作。
 *
 * 输入 / 输出：
 * - 输入：`user_id`、`character_id`。
 * - 输出：`GameHomeOverviewView`，直接供 HTTP 路由返回首页首屏初始化快照。
 *
 * 数据流 / 状态流：
 * - HTTP 鉴权成功 -> 本模块并发读取既有服务 + PostgreSQL 摘要数据 -> 聚合成首页 DTO -> 返回给前端。
 *
 * 复用设计说明：
 * - 首页聚合会同时被前端初始化与后续潜在的服务端预热复用，因此独立成模块，避免路由层直接堆 SQL 与跨领域拼装。
 * - 任务摘要、主线追踪、队伍摘要都属于高频变化点，但又只需要首页最小只读快照；集中在这里后，后续补完整路由时不会把首页耦合进各自 handler。
 *
 * 关键边界条件与坑点：
 * 1. 首页聚合只返回首页已消费的最小快照，不能为了凑“全量对齐”把其它详情接口的大对象一并塞进来。
 * 2. 聚合里的各子模块失败口径要和 Node 保持一致：签到异常直接视为首页失败，其它可降级只读项则保持 `null/空列表`。
 */
