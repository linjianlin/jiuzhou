/**
 * 自动洗炼功能开关
 *
 * 作用（做什么 / 不做什么）：
 * 1. 做什么：集中维护自动洗炼当前是否开放，以及关闭时统一展示的提示文案，避免桌面端、移动端、执行 Hook 各写一份布尔值和提示语。
 * 2. 做什么：为自动洗炼入口显隐与执行拦截提供单一数据源，保证“先暂时屏蔽”只改这一处常量即可恢复。
 * 3. 不做什么：不负责洗炼业务逻辑、不负责普通手动洗炼，也不处理词条池预览。
 *
 * 输入/输出：
 * - 输入：无运行时输入，当前仅通过常量控制。
 * - 输出：自动洗炼是否启用、关闭提示文案。
 *
 * 数据流/状态流：
 * - 本文件常量 -> `useAutoRerollController` 判断是否允许执行；
 * - 本文件常量 -> `BagModal` / `MobileBagModal` 控制入口和弹窗显隐。
 *
 * 关键边界条件与坑点：
 * 1. 该开关只屏蔽“自动洗炼”，不能误伤手动洗炼，否则会扩大本次改动范围。
 * 2. 入口隐藏并不等于绝对安全，执行 Hook 仍需复用同一开关二次拦截，避免遗漏入口时还能触发请求。
 */

export const AUTO_REROLL_FEATURE_ENABLED = false;

export const AUTO_REROLL_FEATURE_DISABLED_MESSAGE = '自动洗炼功能暂时关闭';
